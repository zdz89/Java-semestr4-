
public class DifferentVectorSizeException extends Exception
{
	public DifferentVectorSizeException() { super(); }
	  public DifferentVectorSizeException(String message) { super(message); }
	  public DifferentVectorSizeException(String message, Throwable cause) { super(message, cause); }
	  public DifferentVectorSizeException(Throwable cause) { super(cause); }
}
